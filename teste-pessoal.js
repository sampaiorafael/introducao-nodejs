/**
 * The process object is a global Node.js object that contains 
 * functions and data all related to the currently running Node.js process. 
 * The argv property is an array of strings containing all the command line arguments 
 * given to a program
 * 
 */
 console.log(process.argv)


/**
 * The env object stores all the environment variables 
 * that are available when Node.js is running the program.
 */
 console.log(process.env)